    private void addUser() throws HeadlessException {
        if ("Username".equals(jTextField4.getText()) || "password".equals(jPasswordField1.getText()) || "Name".equals(jTextField3.getText())) {
            JOptionPane.showMessageDialog(null, "You must fill all the fields \n (If your password is 'password' change it)", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            if (EditAccounts.CheckUsername(jTextField4.getText())) {
                JOptionPane.showMessageDialog(null, "this username is used", "ERROR", JOptionPane.ERROR_MESSAGE);
            } else {
                try{
                    //
                    DataBase db = new DataBase();
                    String[] n = jTextField3.getText().split(" ");
                    String sql = "INSERT INTO accounts VALUES (NULL," +"'"+ jTextField4.getText()+"'" + "," +"'"+ jPasswordField1.getText()+"'"
                            + "," +"'"+ n[0] +"'"+ ","+"'"+ n[1]+"'" + ",0)";
                    db.Update_Query(sql);
                    sql = "SELECT * FROM accounts WHERE Username = '"+ jTextField4.getText() +"'";
                    ResultSet r = db.Select_Query(sql);
                    r.next();
                    sql = "INSERT INTO roles VALUES (" +"'"+ jComboBox1.getSelectedItem().toString()+"'" + "," +"'"+ r.getString("UserId")+"'" +")";
                    db.Update_Query(sql);
                    JOptionPane.showMessageDialog(null, "user is added successfully", "DONE", JOptionPane.INFORMATION_MESSAGE);
                    
                } catch (SQLException | ClassNotFoundException ex) {
                    Logger.getLogger(Admin.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            
        }
    }

